package week1.day2;

public class LearnForLoop {

	public static void main(String[] args) {
		// For loop
		for (int i = 1; i <= 5; i++) {
			System.out.println(i);
		}

		System.out.println("----------------------");

		// Reverse for loop

		for (int i = 5; i >= 1; i--) {
       System.out.println(i);
}

	}

}
